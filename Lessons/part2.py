# How does booleans work?
# Evaluate the first value, and it will ONLY look at the 2nd value if the first is true.
# example of this:
# x = True
# y = False
# z = True
# x and y
# the response would be false, because the first value is true, it will then move on to the next and final value (y), which is false.
# y and x, would still be false, as the first value does not allow it to check the second value. If either value is false, thats where it stops.
# the opposite is the boolean operator, "or", this makes it only read the 2nd value if the first value is FALSE. 
# x or y
# True
# y or x
# True
# They are both True because x is true, and y is second, making it not check the 2nd value, and as x is first it will result in True.
# y or x is True because False is the first value, and the "or" operator checks the 2nd value if the first is false, x is next so it is true 
# and ends there.

